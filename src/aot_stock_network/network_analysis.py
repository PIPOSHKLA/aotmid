class NetworkBuilder:
    """Construct a weighted undirected graph from a feature DataFrame.

    Parameters
    ----------
    df : pd.DataFrame
        Feature dataset with time index.
    column_mapping : dict, optional
        {node_name: column_name} mapping. Auto-resolved if None.
    """

    def __init__(
        self,
        df: pd.DataFrame,
        column_mapping: Optional[Dict[str, str]] = None,
    ):
        self._df = df.copy()
        self._node_map = _resolve_columns(df, column_mapping)
        self._graph: Optional[nx.Graph] = None
        self._edge_method: Optional[str] = None
        self._edge_threshold: Optional[float] = None

        logger.info(
            "NetworkBuilder initialized | %d nodes resolved | columns=%s",
            len(self._node_map),
            list(self._node_map.keys()),
        )

    # ── public properties ─────────────────────────────────—

    @property
    def nodes(self) -> List[str]:
        """List of resolved node names."""
        return list(self._node_map.keys())

    @property
    def graph(self) -> Optional[nx.Graph]:
        """Built graph (None before :meth:`build_graph`)."""
        return self._graph

    # ── build_graph ─────────────────────────────────────────

    def build_graph(
        self,
        method: str = "pearson",
        threshold: float = 0.3,
        min_edges: int = 1,
        absolute_value: bool = True,
    ) -> nx.Graph:
        """Build the association graph.

        Parameters
        ----------
        method : {"pearson", "spearman", "mutual_info"}
            Association measure for edge weights.
        threshold : float
            Edges with |weight| < threshold are dropped.
        min_edges : int
            Ensure every node has at least this many edges (iteratively
            lowers threshold within bounds).
        absolute_value : bool
            If True, edges with *negative* Pearson/Spearman are kept
            (weight taken as abs value). Only meaningful for pearson/spearman.

        Returns
        -------
        nx.Graph
            Weighted undirected graph.
        """
        nodes = self.nodes
        if len(nodes) < 2:
            raise ValueError(f"Need ≥2 nodes, got {len(nodes)}")

        columns = [self._node_map[n] for n in nodes]
        data = self._df[columns].dropna()
        logger.info(
            "build_graph: method=%s threshold=%.2f data=%d rows", method, threshold, len(data)
        )

        # ── compute weight matrix ──────────────────────────
        if method == "pearson":
            weight_df = data.corr(method="pearson")
        elif method == "spearman":
            weight_df = data.corr(method="spearman")
        elif method == "mutual_info":
            weight_df = _compute_mutual_information_matrix(data, columns)
        else:
            raise ValueError(
                f"Unknown method '{method}'. Use 'pearson', 'spearman', or 'mutual_info'."
            )

        # Rename to node names
        weight_df.index = nodes
        weight_df.columns = nodes

        # ── threshold ──────────────────────────────────────
        G = nx.Graph()
        G.add_nodes_from(nodes)

        current_threshold = threshold
        for _ in range(20):  # safety limit
            G.clear_edges()
            for i, u in enumerate(nodes):
                for j, v in enumerate(nodes):
                    if j <= i:
                        continue
                    w = weight_df.loc[u, v]
                    use_weight = abs(w) if absolute_value and method != "mutual_info" else w
                    if use_weight >= current_threshold:
                        G.add_edge(u, v, weight=use_weight, raw_weight=w, method=method)

            # Check min_edges
            if min_edges and G.number_of_nodes() > 0:
                min_deg = min(d for _, d in G.degree())
                if min_deg >= min_edges or current_threshold <= 0.05:
                    break
                current_threshold = max(current_threshold * 0.75, 0.0)
                logger.debug(
                    "Lowering threshold to %.3f (min degree=%d)", current_threshold, min_deg
                )
            else:
                break

        self._graph = G
        self._edge_method = method
        self._edge_threshold = current_threshold

        logger.info(
            "build_graph done | %d nodes %d edges | density=%.4f | threshold=%.3f",
            G.number_of_nodes(),
            G.number_of_edges(),
            nx.density(G),
            current_threshold,
        )
        return G

    # ── adjacency matrix ───────────────────────────────────

    def adjacency_matrix(self) -> pd.DataFrame:
        """Return the weighted adjacency matrix as a DataFrame."""
        if self._graph is None:
            raise RuntimeError("Call build_graph() first.")
        return nx.to_pandas_adjacency(self._graph, weight="weight")

    def correlation_matrix(self) -> pd.DataFrame:
        """Return the full correlation/association matrix."""
        if self._graph is None:
            raise RuntimeError("Call build_graph() first.")
        nodes = self.nodes
        columns = [self._node_map[n] for n in nodes]
        data = self._df[columns].dropna()
        method = self._edge_method or "pearson"
        if method == "pearson":
            return data.corr(method="pearson")
        elif method == "spearman":
            return data.corr(method="spearman")
        else:
            return _compute_mutual_information_matrix(data, columns)

    def summary(self) -> str:
        """Text summary of the built network."""
        if self._graph is None:
            return "No graph built yet."
        lines = [
            "NetworkBuilder Summary",
            "======================",
            f"Nodes: {self._graph.number_of_nodes()}",
            f"Edges: {self._graph.number_of_edges()}",
            f"Density: {nx.density(self._graph):.4f}",
            f"Method: {self._edge_method}",
            f"Threshold: {self._edge_threshold:.3f}",
            f"Nodes: {', '.join(self.nodes)}",
        ]
        return "\n".join(lines)


