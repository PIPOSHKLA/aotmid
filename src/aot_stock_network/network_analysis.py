"""
network_analysis.py — Social Network Analysis of AOT Influencing Factors
=========================================================================

Constructs a weighted, undirected graph where each node is a financial or
macroeconomic variable and each edge represents the strength of association
between two variables. Supports multiple association measures, configurable
thresholding, community detection, and full network metric computation.

Nodes (9 macroeconomic / market variables)
-------------------------------------------
  AOT Closing Price     — Monthly close of AOT common stock (SET)
  Trading Volume        — Monthly total trading volume of AOT
  SET Index             — Stock Exchange of Thailand benchmark index
  Tourist Arrivals      — International tourist arrivals to Thailand
  Tourism Revenue       — Revenue generated by international tourism
  USD/THB               — USD / Thai Baht exchange rate
  Policy Rate           — Bank of Thailand policy interest rate
  Inflation (CPI)       — Headline Consumer Price Index
  GDP                   — Gross Domestic Product (quarterly → monthly)

Edges (3 association measures)
-------------------------------
  Pearson  r        — Linear correlation coefficient
  Spearman ρ        — Rank-based monotonic correlation
  Mutual Info       — Non-linear dependence (k-NN estimator)

Configurable threshold — edges with |correlation| < threshold are dropped.

Graph Metrics (computed automatically)
---------------------------------------
  Degree Centrality      — Fraction of nodes a given node is connected to.
                           High-degree nodes are directly influencing many
                           other variables in the system.

  Betweenness Centrality — Fraction of shortest paths that pass through a
                           node. High-betweenness nodes act as bridges or
                           gatekeepers in the network.

  Closeness Centrality   — Reciprocal of the sum of shortest-path distances
                           from a node to all others. High-closeness nodes
                           can quickly propagate shocks through the system.

  Eigenvector Centrality — Measures influence based on connections to other
                           high-influence nodes. A node with high eigenvector
                           centrality is connected to nodes that are themselves
                           central (Google's PageRank is a variant).

  PageRank               — Variant of eigenvector centrality with damping.
                           Captures the idea that a node is important if it
                           is linked from other important nodes.

  Community Detection    — Louvain greedy modularity-maximisation algorithm.
                           Groups nodes with denser intra-community edges
                           than inter-community edges. Each node is assigned
                           a community label (0, 1, 2, …).

  Connected Components   — Sets of nodes reachable via any path. In a fully
                           connected graph there is 1 component.

  Network Density        — Ratio of actual edges to possible edges. Ranges
                           from 0 (empty graph) to 1 (complete graph).

  Average Path Length    — Mean shortest-path distance between all node pairs.
                           Shorter average paths imply faster information flow.

  Graph Diameter         — Longest shortest-path distance. Represents the
                           "worst-case" propagation delay in the system.

Visualization (static + interactive)
-------------------------------------
  • Static matplotlib figure with:
      — Node size ∝ Degree Centrality
      — Node colour = Community (qualitative palette)
      — Edge width ∝ |correlation|
      — Edge colour = positive (blue) / negative (red)
  • Publication-quality (300 dpi, SVG export, LaTeX-style fonts)
  • Interactive Plotly figure with:
      — Hover tooltips: node name, community, all 5 centrality values
      — Zoom / pan
      — Search (ctrl+F in browser)
      — Filter by community / centrality range
      — SVG / PNG export via plotly toolbar

Usage
-----
    from aot_stock_network.network_analysis import NetworkBuilder, NetworkAnalyzer, NetworkVisualizer

    builder = NetworkBuilder(df_features)
    G = builder.build_graph(method="pearson", threshold=0.5)

    analyzer = NetworkAnalyzer(G)
    metrics = analyzer.compute_all()
    print(analyzer.summary())

    visualizer = NetworkVisualizer(G, metrics)
    visualizer.plot_static("reports/figures/network_graph.svg")
    visualizer.plot_interactive("reports/figures/network_interactive.html")
"""

