@dataclass
class NetworkMetrics:
    """Container for all computed graph metrics."""

    degree_centrality: Dict[str, float] = field(default_factory=dict)
    betweenness_centrality: Dict[str, float] = field(default_factory=dict)
    closeness_centrality: Dict[str, float] = field(default_factory=dict)
    eigenvector_centrality: Dict[str, float] = field(default_factory=dict)
    pagerank: Dict[str, float] = field(default_factory=dict)
    community: Dict[str, int] = field(default_factory=dict)
    n_communities: int = 0
    connected_components: int = 1
    network_density: float = 0.0
    average_path_length: Optional[float] = None
    diameter: Optional[int] = None

    def to_dataframe(self) -> pd.DataFrame:
        """Return metrics as a DataFrame (rows = nodes)."""
        nodes = sorted(self.degree_centrality.keys())
        rows = []
        for n in nodes:
            rows.append(
                {
                    "node": n,
                    "degree_centrality": self.degree_centrality.get(n, 0.0),
                    "betweenness_centrality": self.betweenness_centrality.get(n, 0.0),
                    "closeness_centrality": self.closeness_centrality.get(n, 0.0),
                    "eigenvector_centrality": self.eigenvector_centrality.get(n, 0.0),
                    "pagerank": self.pagerank.get(n, 0.0),
                    "community": self.community.get(n, -1),
                }
            )
        return pd.DataFrame(rows).set_index("node")


class NetworkAnalyzer:
    """Compute all graph-theoretic metrics on a NetworkX graph.

    Parameters
    ----------
    G : nx.Graph
        Weighted undirected graph built by NetworkBuilder.
    """

    def __init__(self, G: nx.Graph):
        self._G = G
        self._metrics: Optional[NetworkMetrics] = None

    # ── compute_all ────────────────────────────────────────

    def compute_all(self) -> NetworkMetrics:
        """Compute every metric in one pass.

        Returns
        -------
        NetworkMetrics
            Dataclass with all centrality, community, and global metrics.
        """
        G = self._G
        m = NetworkMetrics()

        n_nodes = G.number_of_nodes()

        # ── Centralities ───────────────────────────────────
        if n_nodes >= 2:
            m.degree_centrality = nx.degree_centrality(G)
            m.betweenness_centrality = nx.betweenness_centrality(
                G,
                weight="weight",
                normalized=True,
            )
            try:
                m.closeness_centrality = nx.closeness_centrality(
                    G,
                    distance="weight",
                )
            except Exception:
                m.closeness_centrality = {n: 0.0 for n in G.nodes()}

            try:
                m.eigenvector_centrality = nx.eigenvector_centrality(
                    G,
                    weight="weight",
                    max_iter=1000,
                    tol=1e-6,
                )
            except Exception:
                m.eigenvector_centrality = {n: 0.0 for n in G.nodes()}

            m.pagerank = nx.pagerank(G, weight="weight")
        else:
            for n in G.nodes():
                v = 1.0 if n_nodes == 1 else 0.0
                m.degree_centrality[n] = v
                m.betweenness_centrality[n] = 0.0
                m.closeness_centrality[n] = v
                m.eigenvector_centrality[n] = v
                m.pagerank[n] = v

        # ── Community Detection (Louvain) ──────────────────
        try:
            comm_map_raw = nx.community.louvain_communities(G, weight="weight", seed=42)
            m.community = {}
            for cid, members in enumerate(comm_map_raw):
                for node in members:
                    m.community[node] = cid
            m.n_communities = len(set(m.community.values()))
        except Exception as exc:
            logger.warning("Community detection failed: %s", exc)
            m.community = {n: 0 for n in G.nodes()}
            m.n_communities = 1

        # ── Connected Components ───────────────────────────
        m.connected_components = nx.number_connected_components(G)

        # ── Global metrics ─────────────────────────────────
        m.network_density = nx.density(G)

        if n_nodes >= 2 and nx.is_connected(G):
            try:
                m.average_path_length = nx.average_shortest_path_length(G, weight="weight")
            except Exception:
                m.average_path_length = None
            try:
                m.diameter = nx.diameter(G, weight="weight")
            except Exception:
                m.diameter = None
        else:
            m.average_path_length = None
            m.diameter = None

        self._metrics = m
        logger.info(
            "NetworkAnalyzer: %d centralities | %d communities | density=%.4f | components=%d",
            n_nodes,
            m.n_communities,
            m.network_density,
            m.connected_components,
        )
        return m

    @property
    def metrics(self) -> Optional[NetworkMetrics]:
        """Last computed metrics."""
        return self._metrics

    def summary(self) -> str:
        """Text summary of all metrics."""
        m = self._metrics
        if m is None:
            return "No metrics computed yet. Call compute_all()."

        lines = [
            "NetworkAnalysis Summary",
            "=======================",
            f"Nodes: {len(m.degree_centrality)}",
            f"Edges: {self._G.number_of_edges()}",
            f"Network Density: {m.network_density:.4f}",
            "",
            "Centrality (top node by each metric):",
        ]
        for label, cent in [
            ("Degree", m.degree_centrality),
            ("Betweenness", m.betweenness_centrality),
            ("Closeness", m.closeness_centrality),
            ("Eigenvector", m.eigenvector_centrality),
            ("PageRank", m.pagerank),
        ]:
            if cent:
                top = max(cent, key=cent.get)
                lines.append(f"  {label:15s} -> {top} ({cent[top]:.4f})")
            else:
                lines.append(f"  {label:15s} -> (empty)")

        lines.append("")
        lines.append(f"Communities: {m.n_communities}")
        if m.community:
            comm_groups: Dict[int, List[str]] = {}
            for node, cid in m.community.items():
                comm_groups.setdefault(cid, []).append(node)
            for cid in sorted(comm_groups):
                lines.append(f"  Community {cid}: {', '.join(comm_groups[cid])}")

        lines.append("")
        lines.append(f"Connected Components: {m.connected_components}")
        lines.append(f"Average Path Length:  {m.average_path_length or 'N/A (disconnected)'}")
        lines.append(f"Graph Diameter:       {m.diameter or 'N/A (disconnected)'}")

        return "\n".join(lines)


