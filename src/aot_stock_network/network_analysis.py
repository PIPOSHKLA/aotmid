    # ── Interactive Plotly graph ───────────────────────────

    def plot_interactive(
        self,
        output_path: Optional[Union[str, Path]] = None,
        title: str = "AOT Influencing Factors — Social Network Graph",
    ) -> "Figure":  # noqa: F821
        """Interactive Plotly network graph with hover, zoom, and filter.

        Features
        --------
        • Hover:  node name, community, all 5 centrality values
        • Zoom:   scroll to zoom, click-drag to pan
        • Search: browser Ctrl+F works on text labels
        • Filter: use plotly legend to toggle communities on/off
        • Export: plotly toolbar (camera icon → SVG / PNG)

        Parameters
        ----------
        output_path : str or Path, optional
            HTML file path for the interactive graph.
        title : str, default "AOT Influencing Factors — Social Network Graph"

        Returns
        -------
        plotly.graph_objects.Figure
        """
        import plotly.graph_objects as go

        G = self._G
        m = self._metrics
        edge_x: List[Optional[float]] = []
        edge_y: List[Optional[float]] = []
        edge_colors_plotly: List[str] = []
        edge_widths_plotly: List[float] = []

        for u, v, d in G.edges(data=True):
            x0, y0 = self._pos[u]
            x1, y1 = self._pos[v]
            edge_x += [x0, x1, None]
            edge_y += [y0, y1, None]
            raw = d.get("raw_weight", d.get("weight", 0.5))
            edge_colors_plotly.append(
                to_hex(PUBLICATION_COLORS["edge_pos"])
                if raw >= 0
                else to_hex(PUBLICATION_COLORS["edge_neg"])
            )
            edge_widths_plotly.append(abs(d.get("weight", 0.5)) * 3 + 0.5)

        # Edge traces per colour (positive / negative) for legend
        pos_edge_x: List[Optional[float]] = []
        pos_edge_y: List[Optional[float]] = []
        neg_edge_x: List[Optional[float]] = []
        neg_edge_y: List[Optional[float]] = []

        for u, v, d in G.edges(data=True):
            x0, y0 = self._pos[u]
            x1, y1 = self._pos[v]
            raw = d.get("raw_weight", d.get("weight", 0.5))
            pts = [x0, x1, None]
            if raw >= 0:
                pos_edge_x += pts
                pos_edge_y += [y0, y1, None]
            else:
                neg_edge_x += pts
                neg_edge_y += [y0, y1, None]

        edge_traces = []
        if pos_edge_x:
            edge_traces.append(
                go.Scatter(
                    x=pos_edge_x,
                    y=pos_edge_y,
                    mode="lines",
                    line=dict(color=to_hex(PUBLICATION_COLORS["edge_pos"]), width=1),
                    hoverinfo="none",
                    showlegend=True,
                    name="Positive Correlation",
                )
            )
        if neg_edge_x:
            edge_traces.append(
                go.Scatter(
                    x=neg_edge_x,
                    y=neg_edge_y,
                    mode="lines",
                    line=dict(color=to_hex(PUBLICATION_COLORS["edge_neg"]), width=1),
                    hoverinfo="none",
                    showlegend=True,
                    name="Negative Correlation",
                )
            )

        # Node trace
        node_x: List[float] = []
        node_y: List[float] = []
        node_text: List[str] = []
        node_sizes_plotly: List[float] = []
        node_colors_plotly: List[str] = []
        node_communities_list: List[int] = []
        community_labels: List[str] = []

        sizes = self._node_sizes(min_size=15, max_size=50)
        colors = self._node_colors()

        for i, node in enumerate(G.nodes()):
            x, y = self._pos[node]
            node_x.append(x)
            node_y.append(y)
            node_sizes_plotly.append(sizes[i])
            node_colors_plotly.append(colors[i])

            cid = m.community.get(node, -1)
            node_communities_list.append(cid)
            community_labels.append(f"Community {cid}")

            deg_c = f"{m.degree_centrality.get(node, 0):.4f}"
            bet_c = f"{m.betweenness_centrality.get(node, 0):.4f}"
            clo_c = f"{m.closeness_centrality.get(node, 0):.4f}"
            eig_c = f"{m.eigenvector_centrality.get(node, 0):.4f}"
            pr_c = f"{m.pagerank.get(node, 0):.4f}"

            node_text.append(
                f"<b>{node}</b><br>"
                f"Community: {cid}<br>"
                f"Degree: {deg_c}<br>"
                f"Betweenness: {bet_c}<br>"
                f"Closeness: {clo_c}<br>"
                f"Eigenvector: {eig_c}<br>"
                f"PageRank: {pr_c}"
            )

        node_trace = go.Scatter(
            x=node_x,
            y=node_y,
            mode="markers+text",
            text=[n for n in G.nodes()],
            textposition="top center",
            textfont=dict(size=10, color="#212121"),
            marker=dict(
                size=node_sizes_plotly,
                color=node_colors_plotly,
                line=dict(color="white", width=1.5),
            ),
            hoverinfo="text",
            hovertext=node_text,
            showlegend=False,
            customdata=node_communities_list,
        )

        # Legend for communities
        community_traces = []
        unique_comm = sorted(set(community_labels))
        comm_color_map = {
            f"Community {i}": colors[i % len(colors)] for i in range(max(node_communities_list) + 1)
        }
        for cl in unique_comm:
            cid = int(cl.split()[-1])
            community_traces.append(
                go.Scatter(
                    x=[None],
                    y=[None],
                    mode="markers",
                    marker=dict(size=10, color=comm_color_map.get(cl, "#888")),
                    showlegend=True,
                    name=cl,
                    legendgroup=cl,
                )
            )

        data = edge_traces + [node_trace] + community_traces

        fig = go.Figure(data=data)
        fig.update_layout(
            title=dict(text=title, font=dict(size=16)),
            showlegend=True,
            hovermode="closest",
            legend=dict(
                title="Legend",
                font=dict(size=10),
                itemsizing="constant",
                groupclick="toggleitem",
            ),
            xaxis=dict(visible=False, showgrid=False, zeroline=False),
            yaxis=dict(visible=False, showgrid=False, zeroline=False),
            plot_bgcolor="white",
            paper_bgcolor="white",
            margin=dict(l=20, r=20, t=60, b=20),
            dragmode="pan",
        )

        # ── export HTML ────────────────────────────────────
        if output_path:
            p = Path(output_path)
            p.parent.mkdir(parents=True, exist_ok=True)
            fig.write_html(
                p,
                include_plotlyjs="cdn",
                config={
                    "scrollZoom": True,
                    "displayModeBar": True,
                    "modeBarButtonsToAdd": ["drawline", "eraseshape"],
                    "toImageButtonOptions": {
                        "format": "svg",
                        "filename": p.stem,
                        "height": 800,
                        "width": 1200,
                    },
                },
            )
            logger.info("Exported interactive graph: %s", p)

        return fig

    # ── export GraphML ─────────────────────────────────────

    def export_graphml(self, output_path: Union[str, Path]) -> Path:
        """Export graph in GraphML format for Gephi / Cytoscape.

        Node attributes: degree, all 5 centralities, community.
        Edge attributes: weight, raw_weight, method.

        Parameters
        ----------
        output_path : str or Path

        Returns
        -------
        Path
            Saved file path.
        """
        p = Path(output_path)
        p.parent.mkdir(parents=True, exist_ok=True)

        G = self._G.copy()
        m = self._metrics

        # Attach node attributes
        for node in G.nodes():
            G.nodes[node]["degree"] = G.degree(node)
            G.nodes[node]["degree_centrality"] = m.degree_centrality.get(node, 0.0)
            G.nodes[node]["betweenness_centrality"] = m.betweenness_centrality.get(node, 0.0)
            G.nodes[node]["closeness_centrality"] = m.closeness_centrality.get(node, 0.0)
            G.nodes[node]["eigenvector_centrality"] = m.eigenvector_centrality.get(node, 0.0)
            G.nodes[node]["pagerank"] = m.pagerank.get(node, 0.0)
            G.nodes[node]["community"] = m.community.get(node, -1)

        nx.write_graphml(G, str(p))
        logger.info("Exported GraphML: %s", p)
        return p

    # ── generate_all ───────────────────────────────────────

    def generate_all(
        self,
        output_dir: Union[str, Path],
        df_adj: Optional[pd.DataFrame] = None,
        prefix: str = "",
    ) -> Dict[str, Path]:
        """Generate all figures and exports in one call.

        Parameters
        ----------
        output_dir : str or Path
        df_adj : pd.DataFrame, optional
            Adjacency matrix (for heatmap). Obtained from
            :meth:`NetworkBuilder.adjacency_matrix()`.
        prefix : str, optional
            File name prefix.

        Returns
        -------
        dict
            {description: Path} mapping.
        """
        out = Path(output_dir)
        out.mkdir(parents=True, exist_ok=True)
        paths: Dict[str, Path] = {}

        n_expected = 5  # static plots
        if df_adj is not None:
            n_expected += 1

        def _plot_and_save(fn, name: str, *args, **kwargs):
            try:
                result = fn(*args, **kwargs)
                if isinstance(result, tuple):
                    fig = result[0]
                else:
                    fig = result
                path = out / f"{name}.svg"
                fig.savefig(path, dpi=300, bbox_inches="tight")
                plt.close(fig)
                paths[name] = path
            except Exception as exc:
                logger.error("Failed to generate '%s': %s", name, exc)

        _plot_and_save(self.plot_static, f"{prefix}network_graph")
        _plot_and_save(self.plot_centrality_heatmap, f"{prefix}centrality_heatmap")
        _plot_and_save(self.plot_centrality_barchart, f"{prefix}centrality_barchart")
        _plot_and_save(self.plot_degree_distribution, f"{prefix}degree_distribution")
        if df_adj is not None:
            _plot_and_save(self.plot_adjacency_heatmap, f"{prefix}adjacency_heatmap", df_adj)

        # Interactive HTML
        try:
            html_path = out / f"{prefix}network_interactive.html"
            self.plot_interactive(html_path)
            paths[f"{prefix}network_interactive"] = html_path
        except Exception as exc:
            logger.error("Failed to generate interactive: %s", exc)

        # GraphML
        try:
            gml_path = out / f"{prefix}network.graphml"
            self.export_graphml(gml_path)
            paths[f"{prefix}network_graphml"] = gml_path
        except Exception as exc:
            logger.error("Failed to export GraphML: %s", exc)

        logger.info("Generated %d/%d exports in %s", len(paths), n_expected + 2, out)
        return paths


