def _resolve_columns(
    df: pd.DataFrame,
    column_mapping: Optional[Dict[str, str]] = None,
) -> Dict[str, str]:
    """Map canonical node names to actual DataFrame columns.

    Parameters
    ----------
    df : pd.DataFrame
        Feature dataset.
    column_mapping : dict, optional
        Explicit {node_name: column_name} mapping.

    Returns
    -------
    dict
        {node_name: column_name} for all successfully resolved nodes.
    """
    if column_mapping:
        resolved = {}
        for node, col in column_mapping.items():
            if col in df.columns:
                resolved[node] = col
            else:
                logger.warning("Column '%s' for node '%s' not found", col, node)
        return resolved

    resolved: Dict[str, str] = {}
    for node, patterns in DEFAULT_COLUMN_PATTERNS.items():
        for pat in patterns:
            if pat in df.columns:
                resolved[node] = pat
                break
    return resolved


def _compute_mutual_information_matrix(
    df: pd.DataFrame,
    columns: List[str],
    random_state: int = 42,
) -> pd.DataFrame:
    """Compute pairwise Mutual Information matrix (k-NN estimator)."""
    n = len(columns)
    mi_mat = np.zeros((n, n))
    arr = df[columns].dropna().values.astype(np.float64)
    for i in range(n):
        X = np.delete(arr, i, axis=1)
        y = arr[:, i]
        mi = mutual_info_regression(X, y, random_state=random_state)
        mi_mat[i, :i] = mi[:i]
        mi_mat[:i, i] = mi[:i]
    return pd.DataFrame(mi_mat, index=columns, columns=columns)


def _rescale(values: np.ndarray, target_min: float = 0.5, target_max: float = 1.0) -> np.ndarray:
    """Min-max rescale to [target_min, target_max]; constant input → target_max."""
    lo, hi = values.min(), values.max()
    if np.isclose(lo, hi):
        return np.full_like(values, target_max)
    return target_min + (values - lo) / (hi - lo) * (target_max - target_min)


