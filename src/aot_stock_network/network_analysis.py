def run_network_pipeline(
    df: pd.DataFrame,
    column_mapping: Optional[Dict[str, str]] = None,
    method: str = "pearson",
    threshold: float = 0.3,
    output_dir: Union[str, Path] = "reports/figures",
    generate_exports: bool = True,
) -> Dict[str, Any]:
    """Run the full network analysis pipeline in one call.

    Parameters
    ----------
    df : pd.DataFrame
        Feature dataset.
    column_mapping : dict, optional
        {node_name: column_name}.
    method : str, default "pearson"
        Association measure.
    threshold : float, default 0.3
        Edge weight threshold.
    output_dir : str or Path, default "reports/figures"
        Export directory.
    generate_exports : bool, default True
        If True, generate all figures and exports.

    Returns
    -------
    dict with keys "builder", "analyzer", "visualizer", "metrics", "paths".
    """
    builder = NetworkBuilder(df, column_mapping)
    G = builder.build_graph(method=method, threshold=threshold)

    analyzer = NetworkAnalyzer(G)
    metrics = analyzer.compute_all()

    visualizer = NetworkVisualizer(G, metrics)
    paths: Dict[str, Path] = {}
    if generate_exports:
        paths = visualizer.generate_all(output_dir, df_adj=builder.adjacency_matrix())

    return {
        "builder": builder,
        "analyzer": analyzer,
        "visualizer": visualizer,
        "metrics": metrics,
        "paths": paths,
    }


